static inline int point_collides(int x, int y) {

	#define IS_SOLID(sheet) ((sheet) != 0 && (sheet) != 4)

	if (x < 0 || x >= level->map_width * 16)
		return 1;

	if (y < 0 || y >= level->map_height * 16)
		return 0;

	return IS_SOLID(level->sheet_map[x / 16 + y / 16 * level->map_width]);
}

static int aabb_collides(int w, int h, int x, int y) {

	w--;
	h--;

	return point_collides(x, y) 		// top, left/right
		|| point_collides(x + w, y)
		|| point_collides(x, y + h / 2) // middle, left/right (otherwise, you can slide between 1-tall terrain)
		|| point_collides(x + w, y + h / 2)
		|| point_collides(x, y + h) 	// bottom, left/right
		|| point_collides(x + w, y + h);
}

void process_level(unsigned long time, Input *input) {

	/*
	 * static player state changes
	 */

	// TEMP toggle between having and not having charge power with cancel
	if (input->cancel && input->cancel_justchanged)
		has_charge_power = !has_charge_power;

	// crouch input
	if (input->down && input->down_justchanged) {
		crouched = TRUE;
	} else if (!input->down && !aabb_collides(16, STANDING_HEIGHT, player_x, player_y)) {
		crouched = FALSE;
	}

	// calculate bounding box
	int player_aabb_yoff = crouched ? (STANDING_HEIGHT - CROUCHING_HEIGHT) : 0;
	int player_aabb_h    = crouched ? CROUCHING_HEIGHT : STANDING_HEIGHT;

	if (input->up && input->up_justchanged) {

		// register a jump at this time
		time_of_last_pressed_jump = time;
	}

	if (aabb_collides(16, player_aabb_h, player_x, player_y + player_aabb_yoff + 1)) {

		// register grounded at this time
		time_of_last_grounded = time;
	}

	/*
	 * update player velocity
	 */

	// if recently pressed jump (input caching) AND recently was on ground (coyote time), jump
	if (time - time_of_last_pressed_jump < 10 && time - time_of_last_grounded < 10) {

		player_dy = JUMP_SPEED;
		time_of_last_jump = time;
		time_of_last_grounded = 0;
	}

	// gravity (if recently jumped, make gravity lesser if holding jump, otherwise greater)
	// ("recently jumped" is longer the faster you're running)
	// TODO stop hardcoding these values
	if ((time - time_of_last_jump) < 10 + ABS(player_dx) * 4) {

		player_dy += input->up ? HIGH_JUMP_GRAVITY : FAST_FALL_GRAVITY;

	} else {

		player_dy += input->down ? FAST_FALL_GRAVITY : DEFAULT_GRAVITY;
	}

	if (crouched) {

		// crouch jumping
		if (input->left ^ input->right) {

			if (time_of_last_grounded != time)
				player_dx = player_dx * SLIPPERINESS + MAX_RUN_SPEED * (1.0 - SLIPPERINESS) * (input->left ? -1 : 1);
			flipped = input->left;
		}

		player_dx *= SLIPPERINESS; // punish the player for crouch jumping by making it always drag, even when inputing >:)

	} else {

		// running animation
		run_cycle_timer += (ABS(player_dx) + 1) / 20;
	
		if (run_cycle_timer >= 4.0) {
			run_cycle_timer -= 4.0;
		}

		// running
		if (input->left ^ input->right) {

			// update velocity
			player_dx = player_dx * SLIPPERINESS + MAX_RUN_SPEED * (1.0 - SLIPPERINESS) * (input->left ? -1 : 1);
			flipped = input->left;

			// charge power (immediately start charging in direction you're inputing if not already, even when airborne!)
			if (has_charge_power && ABS(player_dx) < MIN_CHARGE_SPEED && input->confirm && input->confirm_justchanged) {

				player_dx = (MAX_RUN_SPEED + 0.5) * (input->left ? -1 : 1);
			}

		} else
			player_dx *= SLIPPERINESS;
	}

	/*
	 * apply player velocity
	 */

	// move player w/ collision
	collided_horizontally = FALSE;

	// charging while grounded (and not crouching); instead of stopping horizontally on wall collision, you bounce back + up
	if (!crouched && ABS(player_dx) > MIN_CHARGE_SPEED && aabb_collides(16, STANDING_HEIGHT, player_x, player_y + 1.0)) {

		if (aabb_collides(16, STANDING_HEIGHT, player_x + player_dx, player_y)) {

			int index1 = (int) (player_x / level->sprite_width + 0.5 + (player_dx > 0 ? 1 : -1)) + (int) (player_y / level->sprite_height + 0.5) * level->map_width;
			int index2 = index1 + level->map_width;

			if (level->sheet_map[index1] == 2 && level->sheet_map[index2] == 2) {

				// destroy destructable blocks (sheet index 2)
				level->sheet_map[index1] = 0;
				level->sheet_map[index2] = 0;

			} else {

				// bounce
				player_dx *= -1;
				player_dy -= 1.5;
			}
		}

	// prevent player from moving horizontally through a wall
	} else {

		while (aabb_collides(16, player_aabb_h, player_x + player_dx, player_y + player_aabb_yoff)) {

			player_dx *= 0.7;
			collided_horizontally = TRUE;

			if (ABS(player_dx) < 0.1)
				player_dx = 0;
		}
	}
	player_x += player_dx;

	while (aabb_collides(16, player_aabb_h, player_x, player_y + player_aabb_yoff + player_dy)) {

		player_dy *= 0.7;

		if (ABS(player_dy) < 0.1)
				player_dy = 0;
	}
	player_y += player_dy;

	// respawn if fell out of level
	if (player_y > 16 * LEVEL_HEIGHT) {
		player_x = 40;
		player_y = 148;
	}

	/*
	 * update camera
	 */
	int player_middle_pos = WIDTH / 2 - ((int) player_x + 8);

	while (player_middle_pos + camera_x >= 20 && camera_x > 0) { camera_x--; }
	while (player_middle_pos + camera_x <= -20 && camera_x < LEVEL_WIDTH * level->sprite_width - WIDTH) { camera_x++; }

	/*
	 * determine animation frame
	 */
	if (crouched) {

		// crouching
		player_sprite_index = 7;

	} else if (time_of_last_grounded != time) {

		// airborne
		run_cycle_timer = 0.0;
		player_sprite_index = 4;

	} else if ((ABS(player_dx) < 0.2 && !input->left && !input->right) || collided_horizontally) {

		// not moving
		run_cycle_timer = 0.0;
		player_sprite_index = 0;

	} else {

		// running AND charging
		if (ABS(player_dx) > MIN_CHARGE_SPEED) {

			player_sprite_index = 5 + ((int) run_cycle_timer) % 2;

		// running
		} else {

			player_sprite_index = run_cycle_timer < 3.0 ? 1 + (int) run_cycle_timer : 2;
		}
	}

	if (has_charge_power)
		player_sprite_index += 8;
}

void draw_level(unsigned long time) {

	// draw level sprite map
	draw_sprite_map(level, -camera_x, 0);

	// render player
	draw_sprite_from_sheet(
		player_sprite,
		player_sprite_index,
		(int) player_x - camera_x,
		(int) player_y - 4,
		flipped
	);

	// draw text
    draw_text(font, "ARROW KEYS TO MOVE\nCONFIRM: Z\nCANCEL:  X\nMENU:    C\n(WHAT'S THAT?! +-*/1234567890)", 4, 4);
}



void init() {

	init_level();
}

void process(Input *input) {

	if (input->menu && input->menu_justchanged) {
		paused = !paused;
	}

	if (!paused)
    	process_level(++time, input);

	draw_level(time);

	if (paused)
		draw_text(font, "PAUSED", WIDTH / 2 - 18, HEIGHT / 2 - 3);
}